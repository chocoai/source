package com.jeesite.modules.asset.tianmao.entity;

/**
 * 类描述
 *
 * @author Jace Xiong
 */
public class BigItem {
    private String id;
    private String body;
    private String time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
