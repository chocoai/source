package com.jeesite.modules.asset.tianmao.entity;

/**
 * 商品价签 - 家具价签
 */
public class GoodsTag {
    /**
     * 名称
     */
    private String materielName;

    /**
     * 产品英文名
     */
    private String empName;

    /**
     * 规格名称
     */
    private String specName;
    /**
     * 长
     */
    private String materielLength;

    /**
     * 宽
     */
    private String materielWidth;

    /**
     * 高
     */
    private String materielHeight;

    /**
     * 颜色
     */
    private String color;

    /**
     * 产品产地
     */
    private String productOrigin;

    /**
     * 编码
     */
    private String materielCode;

    /**
     * 金额
     */
    private String money;

    /**
     * 商品链接地址
     */
    private String linkAddress;

    /**
     * 材质
     */
    private String texture;

    public String getMaterielName() {
        return materielName;
    }

    public void setMaterielName(String materielName) {
        this.materielName = materielName;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getSpecName() {
        return specName;
    }

    public void setSpecName(String specName) {
        this.specName = specName;
    }

    public String getMaterielLength() {
        return materielLength;
    }

    public void setMaterielLength(String materielLength) {
        this.materielLength = materielLength;
    }

    public String getMaterielWidth() {
        return materielWidth;
    }

    public void setMaterielWidth(String materielWidth) {
        this.materielWidth = materielWidth;
    }

    public String getMaterielHeight() {
        return materielHeight;
    }

    public void setMaterielHeight(String materielHeight) {
        this.materielHeight = materielHeight;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getProductOrigin() {
        return productOrigin;
    }

    public void setProductOrigin(String productOrigin) {
        this.productOrigin = productOrigin;
    }

    public String getMaterielCode() {
        return materielCode;
    }

    public void setMaterielCode(String materielCode) {
        this.materielCode = materielCode;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getLinkAddress() {
        return linkAddress;
    }

    public void setLinkAddress(String linkAddress) {
        this.linkAddress = linkAddress;
    }

    public String getTexture() {
        return texture;
    }

    public void setTexture(String texture) {
        this.texture = texture;
    }
}
